using System;
using System.Collections.Generic;
using System.Text;

namespace RefreshRepeats
{
	class Refresh 
		{
			public static void Main(string[] args)
				{
					uint i=0;
					i=i+1;
					do
						{
							System.Console.Out.WriteLine("Hello World!! \n");
							i=i+1;
						}while(i<=10);
				}
		}
}